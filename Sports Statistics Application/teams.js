function calculatePCT() {
  teams.forEach((team) => {
    team.PCT = ((team.W / (team.W + team.L)) * 100).toFixed(1);
  });
}

calculatePCT();

function calculateRank(conf) {
  let t = teams.filter((team) => team.conference === conf);
  t.sort((a, b) => {
    return b.PCT - a.PCT;
  });
  for (let i = 0; i < t.length; i++) {
    t[i].rank = i + 1;
  }
}

